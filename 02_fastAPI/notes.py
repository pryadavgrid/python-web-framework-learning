# What is Async/Await 
# In programming (especially JavaScript), Async/Await is a way to handle "asynchronous" tasks. These are tasks that take time to finish—like fetching data from a database, downloading a file, or calling an API.

# async: When you put this keyword before a function, it tells the program: "Hey, this function is going to return a Promise." It ensures the function doesn't block the rest of your code from running.

# await: This can only be used inside an async function. It tells the program: "Wait right here until this specific task finishes before moving to the next line."